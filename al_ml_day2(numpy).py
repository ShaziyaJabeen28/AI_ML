#NumPy

import numpy as np

print(np.__version__) #for version

#1D Array
a = np.array([4,5,6,7,3,4,5,6])
print(a)

#dimension
print(a.ndim)

#shape
print(a.shape)

#size
print(a.size)

#datatype

print(a.dtype)

#2D Array
